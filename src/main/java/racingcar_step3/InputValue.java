package racingcar_step3;

public class InputValue {
    private int carNum;
    private int tryNum;

    public InputValue(int carNum, int tryNum) {
        this.carNum = carNum;
        this.tryNum = tryNum;
    }

    public int getCarNum() {
        return carNum;
    }

    public int getTryNum() {
        return tryNum;
    }
}
