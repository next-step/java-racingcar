package com.devchang202.calculator;

public class Calculator {
    int add(int a, int b) {
        return a + b;
    }

    int substrate (int a, int b){
        return a - b;
    }

    int divide (int a, int b) {
        return a / b;
    }

    int multiply(int a, int b){
        return a * b;
    }
}
