package calc;

public class Calculator {

    public static int add(int pre, int post) {
        return pre + post;
    }

    public static int subtract(int pre, int post) {
        return pre - post;
    }

    public static int multiply(int pre, int post) {
        return pre * post;
    }

    public static int divide(int pre, int post) {
        return  pre / post;
    }
}
