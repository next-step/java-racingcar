package calculator;

public class Calculator {
    public static int add(int i, int j) {
        return i + j;
    }

    public static int min(int i, int j) {
        return i-j;
    }

    public static int mul(int i, int j) {
        return i*j;
    }

    public static int div(int i, int j) {
        return i/j;
    }
}
