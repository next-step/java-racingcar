package calculator;

public class Calculator {

    static int add( int i, int j ) {
        return i + j;
    }

    static int subtract( int i, int j ) {
        return i - j;
    }

    static int multiply( int i, int j ) {
        return i * j;
    }

    static int divide( int i, int j ) {
        return i / j;
    }
}