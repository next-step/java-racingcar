package calculator;

public class Calculator {
    public int add(int first, int second) {
        return first + second;
    }

    public int sub(int first, int second) {
        return first - second;
    }

    public int multiply(int first, int second) {
        return first * second;
    }

    public int divide(int first, int second) {
        return first / second;
    }
}
