package calculator;

public class Calculator {

    static int add(int i, int j) {
        return i + j;
    }

    static int sub(int i, int j) {
        return i - j;
    }

    static int mul(int i, int j) {
        return i * j;
    }

    static int div(int i, int j) {
        return i / j;
    }
}
