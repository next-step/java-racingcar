package calculator;

public class Calculator {
    public int addtion(int num1, int num2) {
        return num1 + num2;
    }

    public int subtract(int num1, int num2) {
        return num1 - num2;
    }

    public int division(int num1, int num2) {
        return num1 / num2;
    }

    public int multiplication(int num1, int num2) {
        return num1 * num2;
    }

}
