package calculator;

public class Calculator {

    public int add(int left, int right) {
        return left + right;
    }

    public int substract(int left, int right) {
        return left - right;
    }

    public int multiple(int left, int right) {
        return left * right;
    }

    public int divide(int left, int right) {
        return left / right;
    }

}
