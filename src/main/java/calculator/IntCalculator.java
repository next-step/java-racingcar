package calculator;

public class IntCalculator {
    public int add(int i, int j) {
        return i + j;
    }

    public int subtract(int i, int j) {
        return i - j;
    }

    public int multiplication(int i, int j) {
        return i * j;
    }

    public int division(int i, int j) {
        return i / j;
    }
}
