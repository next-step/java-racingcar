package car;

public class Car {
    public String name = "";
    public int km = 0;

    public void setKm(int km) {
        this.km = km;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getKm() {
        return this.km;
    }

    public String getName() {
        return this.name;
    }
}
