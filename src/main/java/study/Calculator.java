package study;

public class Calculator {


    public int plus(int a, int b) {
        return a+b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int multiplied(int a, int b) {
        return a * b;
    }

    public int divide(int a, int b) {
        return a / b;
    }
}
